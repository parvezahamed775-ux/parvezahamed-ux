/* --- CORE FUNCTIONS & TRANSLATION --- */

function showToast(message, type = 'success') {
  const toast = document.createElement('div');
  toast.className = `toast ${type === 'error' ? 'toast-error' : ''}`;
  toast.innerHTML = `
    <span>${message}</span>
    <span class="toast-close">&times;</span>
  `;
  
  toast.querySelector('.toast-close').addEventListener('click', () => toast.remove());
  dom.toastsList.appendChild(toast);
  
  // Auto dismiss after 3.5 seconds
  setTimeout(() => {
    if (toast.parentElement) toast.remove();
  }, 3500);
}

// Translate all elements with data-i18n attributes
function translateDOM() {
  const elements = document.querySelectorAll('[data-i18n]');
  elements.forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (I18N[state.lang] && I18N[state.lang][key] !== undefined) {
      el.innerText = I18N[state.lang][key];
    }
  });

  // Handle placeholders
  const inputs = document.querySelectorAll('[data-i18n-placeholder]');
  inputs.forEach(input => {
    const key = input.getAttribute('data-i18n-placeholder');
    if (I18N[state.lang] && I18N[state.lang][key] !== undefined) {
      input.setAttribute('placeholder', I18N[state.lang][key]);
    }
  });

  // Update page title
  document.title = I18N[state.lang].page_title;
}

// Convert numbers dynamically to Bengali digit characters
function parseNumber(num) {
  if (state.lang !== 'bn') return num.toString();
  const digits = { '0': '০', '1': '১', '2': '২', '3': '৩', '4': '৪', '5': '৫', '6': '৬', '7': '৭', '8': '৮', '9': '৯' };
  return num.toString().split('').map(d => digits[d] || d).join('');
}

// Switch between SPA views
function switchView(viewName) {
  state.currentView = viewName;
  
  // Hide all views first
  dom.shopView.classList.remove('active');
  dom.checkoutView.classList.remove('active');
  dom.ordersView.classList.remove('active');
  dom.adminView.classList.remove('active');

  // Remove active styling on nav
  dom.navShop.classList.remove('active');
  dom.navOrders.classList.remove('active');
  dom.navAdmin.classList.remove('active');

  // Hide or show Hero banner
  if (viewName === 'shop') {
    dom.heroBanner.style.display = 'block';
    dom.shopView.classList.add('active');
    dom.navShop.classList.add('active');
    renderShop();
  } else {
    dom.heroBanner.style.display = 'none';
    
    if (viewName === 'checkout') {
      dom.checkoutView.classList.add('active');
      renderCheckout();
    } else if (viewName === 'orders') {
      dom.ordersView.classList.add('active');
      dom.navOrders.classList.add('active');
      renderOrders();
    } else if (viewName === 'admin') {
      dom.adminView.classList.add('active');
      dom.navAdmin.classList.add('active');
      renderAdmin();
    }
  }

  // Scroll to top of app
  window.scrollTo({ top: 0, behavior: 'smooth' });
}

