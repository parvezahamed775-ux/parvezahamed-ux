function addToCart(productId) {
  const prod = getProductById(productId);
  if (!prod) return;

  const cartIndex = state.cart.findIndex(item => item.id === productId);
  
  if (cartIndex !== -1) {
    // Check if new quantity exceeds stock
    if (state.cart[cartIndex].quantity >= prod.stock) {
      showToast(I18N[state.lang].no_stock_alert, 'error');
      return;
    }
    state.cart[cartIndex].quantity++;
  } else {
    if (prod.stock <= 0) {
      showToast(I18N[state.lang].no_stock_alert, 'error');
      return;
    }
    state.cart.push({ id: productId, quantity: 1 });
  }

  saveCart();
  showToast(I18N[state.lang].add_success);
}

function saveCart() {
  localStorage.setItem('fresh_fruits_cart', JSON.stringify(state.cart));
  updateCartCounter();
  renderCart();
}

function updateCartCounter() {
  const totalQty = state.cart.reduce((sum, item) => sum + item.quantity, 0);
  dom.cartCounter.innerText = parseNumber(totalQty);
}

function renderCart() {
  if (state.cart.length === 0) {
    dom.cartItemsContainer.innerHTML = `
      <div class="cart-empty">
        <svg viewBox="0 0 24 24" width="48" height="48" stroke="currentColor" stroke-width="2" fill="none" style="margin: 0 auto 1rem auto; display: block;">
          <circle cx="9" cy="21" r="1"></circle><circle cx="20" cy="21" r="1"></circle>
          <path d="M1 1h4l2.68 13.39a2 2 0 0 0 2 1.61h9.72a2 2 0 0 0 2-1.61L23 6H6"></path>
        </svg>
        <span data-i18n="order_empty_cart">Your cart is empty!</span>
      </div>
    `;
    // Update summary totals to zero
    updateCartCalculations(0);
    dom.btnGoToCheckout.disabled = true;
    translateDOM();
    return;
  }

  dom.btnGoToCheckout.disabled = false;
  
  let subtotal = 0;

  dom.cartItemsContainer.innerHTML = state.cart.map(item => {
    const prod = getProductById(item.id);
    if (!prod) return '';

    const priceTotal = prod.price * item.quantity;
    subtotal += priceTotal;

    const name = state.lang === 'bn' ? prod.nameBN : prod.nameEN;
    const unit = state.lang === 'bn' ? prod.unitBN : prod.unitEN;

    return `
      <div class="cart-item" data-id="${item.id}">
        <div class="cart-item-img">
          ${getFruitSVG(prod.svgType, 45, 45)}
        </div>
        <div class="cart-item-details">
          <h4 class="cart-item-name">${name}</h4>
          <span class="cart-item-price">৳${parseNumber(prod.price)} / ${unit}</span>
          <div class="cart-item-qty">
            <button class="qty-btn btn-qty-dec" data-id="${item.id}">-</button>
            <span class="qty-val">${parseNumber(item.quantity)}</span>
            <button class="qty-btn btn-qty-inc" data-id="${item.id}">+</button>
          </div>
        </div>
        <div class="btn-remove-item" data-id="${item.id}" data-i18n="delete">${I18N[state.lang].delete}</div>
      </div>
    `;
  }).join('');

  // Cart math calculations
  updateCartCalculations(subtotal);

  // Bind cart item actions
  dom.cartItemsContainer.querySelectorAll('.btn-qty-dec').forEach(btn => {
    btn.addEventListener('click', () => {
      const id = btn.getAttribute('data-id');
      const index = state.cart.findIndex(i => i.id === id);
      if (index !== -1) {
        if (state.cart[index].quantity > 1) {
          state.cart[index].quantity--;
        } else {
          state.cart.splice(index, 1);
        }
        saveCart();
      }
    });
  });

  dom.cartItemsContainer.querySelectorAll('.btn-qty-inc').forEach(btn => {
    btn.addEventListener('click', () => {
      const id = btn.getAttribute('data-id');
      const prod = getProductById(id);
      const index = state.cart.findIndex(i => i.id === id);
      if (index !== -1 && prod) {
        if (state.cart[index].quantity >= prod.stock) {
          showToast(I18N[state.lang].no_stock_alert, 'error');
          return;
        }
        state.cart[index].quantity++;
        saveCart();
      }
    });
  });

  dom.cartItemsContainer.querySelectorAll('.btn-remove-item').forEach(btn => {
    btn.addEventListener('click', () => {
      const id = btn.getAttribute('data-id');
      const index = state.cart.findIndex(i => i.id === id);
      if (index !== -1) {
        state.cart.splice(index, 1);
        saveCart();
      }
    });
  });
}

function updateCartCalculations(subtotal) {
  const delivery = subtotal > 0 ? 50 : 0;
  const discount = Math.round(subtotal * (state.discountPercent / 100));
  const total = Math.max(0, subtotal - discount + delivery);

  dom.cartSubtotal.innerText = `৳${parseNumber(subtotal)}`;
  dom.cartDiscount.innerText = `-৳${parseNumber(discount)}`;
  dom.cartDelivery.innerText = `৳${parseNumber(delivery)}`;
  dom.cartTotal.innerText = `৳${parseNumber(total)}`;
}

